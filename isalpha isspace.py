#barra de espaço
a = input ('teste a barra de espaço: ')
print ('o tipo primitivo desse valor é ', type(a))
print ('sua barra de espaço esta funcionando ', a.isspace())
#teclado
b = input ('teste uma letra: ')
print ('o tipo primitivo desse valor é ', type(b))
print ('as letras estão funcionando? ', b.isalpha())
#teclado numerico
c = input ('digite um numero: ')
print ('o tipo primitivo desse valor é ', type(c))
print ('seu teclado numerico esta funcionando? ', c.isnumeric())
